public class AttributeDataTypePojo {
	
	private String attributeDataTypeName;
	private String dataTypeValues;
	private String action;
	public String getAttributeDataTypeName() {
		return attributeDataTypeName;
	}
	public void setAttributeDataTypeName(String attributeDataTypeName) {
		this.attributeDataTypeName = attributeDataTypeName;
	}
	public String getDataTypeValues() {
		return dataTypeValues;
	}
	public void setDataTypeValues(String dataTypeValues) {
		this.dataTypeValues = dataTypeValues;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	
	

}
