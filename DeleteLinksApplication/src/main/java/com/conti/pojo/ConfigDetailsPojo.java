package com.conti.pojo;

public class ConfigDetailsPojo {
	
	private String userName; 
	private String baselineName;	
	private String changeSetName;		
	private String deliverChangeSet;
	private String inputFileName;		
	private String password;	
	private String repositoryUrl;	
	private String attributeMappingFileName;
	private String stateSelected;
	
	public String getAttributeMappingFileName() {
		return attributeMappingFileName;
	}
	public void setAttributeMappingFileName(String attributeMappingFileName) {
		this.attributeMappingFileName = attributeMappingFileName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getBaselineName() {
		return baselineName;
	}
	public void setBaselineName(String baselineName) {
		this.baselineName = baselineName;
	}
	public String getChangeSetName() {
		return changeSetName;
	}
	public void setChangeSetName(String changeSetName) {
		this.changeSetName = changeSetName;
	}
	public String getDeliverChangeSet() {
		return deliverChangeSet;
	}
	public void setDeliverChangeSet(String deliverChangeSet) {
		this.deliverChangeSet = deliverChangeSet;
	}
	public String getInputFileName() {
		return inputFileName;
	}
	public void setInputFileName(String inputFileName) {
		this.inputFileName = inputFileName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getRepositoryUrl() {
		return repositoryUrl;
	}
	public void setRepositoryUrl(String repositoryUrl) {
		this.repositoryUrl = repositoryUrl;
	}
	public String getStateSelected() {
		return stateSelected;
	}
	public void setStateSelected(String stateSelected) {
		this.stateSelected = stateSelected;
	}

	
	

}
