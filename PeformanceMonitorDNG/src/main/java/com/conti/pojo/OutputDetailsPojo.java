package com.conti.pojo;

public class OutputDetailsPojo {
	
	private String BAName;
	private double smallModuleTimeValue;
	private double largeModuleTimeValue;
	
	public String getBAName() {
		return BAName;
	}
	public void setBAName(String bAName) {
		BAName = bAName;
	}
	public double getSmallModuleTimeValue() {
		return smallModuleTimeValue;
	}
	public void setSmallModuleTimeValue(double smallModuleTimeValue) {
		this.smallModuleTimeValue = smallModuleTimeValue;
	}
	public double getLargeModuleTimeValue() {
		return largeModuleTimeValue;
	}
	public void setLargeModuleTimeValue(double largeModuleTimeValue) {
		this.largeModuleTimeValue = largeModuleTimeValue;
	}
	

}
